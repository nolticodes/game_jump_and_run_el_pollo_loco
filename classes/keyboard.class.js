/**
 * Represents the current state of user keyboard inputs.
 */
class Keyboard {
    /**
     * Indicates if the left movement key is pressed.
     */
    left = false;
    /**
     * Indicates if the right movement key is pressed.
     */
    right = false;
    /**
     * Indicates if the jump key is pressed.
     */
    up = false; 
    /**
     * Indicates if the down key is pressed.
     */
    down = false;
    /**
     * Indicates if the space key is pressed.
     */ 
    space = false
    /**
     * Indicates if the throw action key is pressed.
     */
    t = false
}

